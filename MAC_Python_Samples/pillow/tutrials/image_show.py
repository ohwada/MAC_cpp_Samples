import os, sys
from PIL import Image


def show_image(fpath):
	im = Image.open(fpath)
	print(im.format, im.size, im.mode)
	im.show()
# end


# main
args = sys.argv
argc = len(args)
script = args[0]

if argc < 2:
	print("Usage: python %s  <image filepath>" % script)
	exit()
# end

fpath = args[1]
show_image(fpath)
